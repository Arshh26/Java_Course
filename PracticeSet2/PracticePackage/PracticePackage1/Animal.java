package PracticePackage.PracticePackage1;

public class Animal {
    public String eat;
    public String walk;

    public Animal() {
        this.eat = eat;
        this.walk = walk;
    }

    @Override
    public String toString() {
        return "Animal{" +
                "eat='" + eat + '\'' +
                ", walk='" + walk + '\'' +
                '}';


    }
}
