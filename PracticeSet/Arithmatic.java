public class Arithmatic {
    public static void main(String[] args) {
        int a = 12;
        int b = 8;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);


        double x = 7;
        double y = 8;

        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);


    }
}
