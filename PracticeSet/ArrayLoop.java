import java.util.Scanner;

public class ArrayLoop {
    public static void main(String[] args) {
        System.out.println("Array with loop");
        int[] number = {2, 4, 6, 8, 10};
        int i = 0;
        while(i < number.length){
            System.out.println(number[i]);
            i++;
        }


    }
}
